/** Webserver
 * Web server là máy chủ cài đặt các chương trình phục vụ các ứng dụng web
 * Webserver có khả năng tiếp nhận request từ các trình duyệt web
 * và gửi phản hồi đến client thông qua giao thức HTTP hoặc các giao thức khác.
 */

/** 
 * API: Applicaion Programs Interface
*/

/**
 * HTTP request
 * Request trong tiếng Anh nghĩa là lời yêu cầu, lời đề nghị.
 * Đây là thông tin từ client gửi đến server, yêu cầu server làm một việc gì đó.
 * Các phương thức như: GET, POST, PUT,...
*/

/**
 * HTTP Response
 * Response trong tiếng Anh nghĩa là “lời phản hồi“.
 * HTTP Response được gọi là “thông báo phản hồi HTTP“. Đây là kết quả server trả về cho client.
 * Các đối tượng: HTTP-version, Status-Code, Reason-Phrase
 * Một số mã lõi: 1xx là thông tin, 2xx là thành công, 3xx là lỗi điều hướng, 4xx là client, 5xx là lỗi server
 */